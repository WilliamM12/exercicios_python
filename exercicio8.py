#8) Faça um Programa que pergunte quanto você ganha por hora e o número de horas trabalhadas no mês. Calcule e mostre o total do seu salário no referido mês.

g = float(input('Digite o valor que você ganhar por hora:'))
n = int(input('Digite o número de horas que você trabalhou no mês:'))

salario = (g *n)

print('O salário referido por mês é :', salario)